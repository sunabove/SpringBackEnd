
public class Ex007_CompareExample {

	public static void main(String[] args) {
		System.out.println( 1 == 1 ) ;
		System.out.println( 1 == 2 ) ; 
		System.out.println( 1 != 2 ) ;
		
		System.out.println( "-------------" ) ;
		
		int a = 1 ;
		int b = 2 ; 
		
		System.out.println( a == a ) ;
		System.out.println( a == b ) ; 
		System.out.println( a != b ) ;
	}

}
